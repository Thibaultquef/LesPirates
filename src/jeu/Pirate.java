package jeu;

public class Pirate {
	private int pointDeVie = 5;
	private String nom;
	private Couleur couleur;
	private Case caze;
	private Arme arme;
	
	public int getPV() {
		return pointDeVie;
	}
	
	public String getNom() {
		return nom;
	}
	
	public Couleur getCouleur() {
		return couleur;
	}
	
	public Arme getArme() {
		return arme;
	}
	
	public Case caze() {
		return caze;
	}
}
